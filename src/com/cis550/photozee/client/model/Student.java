package com.cis550.photozee.client.model;

import java.io.Serializable;
import java.sql.Timestamp;

public class Student implements Serializable {
	
	public Student(){}
	
	private Timestamp join_year;
	private Timestamp grad_year;
	private String major;
	private Double gpa;
	private Advisorsees advisor;
	
	
	public Timestamp getJoin_year() {
		return join_year;
	}
	public void setJoin_year(Timestamp join_year) {
		this.join_year = join_year;
	}
	public Timestamp getGrad_year() {
		return grad_year;
	}
	public void setGrad_year(Timestamp grad_year) {
		this.grad_year = grad_year;
	}
	public String getMajor() {
		return major;
	}
	public void setMajor(String major) {
		this.major = major;
	}
	public Double getGpa() {
		return gpa;
	}
	public void setGpa(Double gpa) {
		this.gpa = gpa;
	}
	public Advisorsees getAdvisor() {
		return advisor;
	}
	public void setAdvisor(Advisorsees advisor) {
		this.advisor = advisor;
	}
	
	

}
