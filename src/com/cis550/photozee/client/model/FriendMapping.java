package com.cis550.photozee.client.model;

import java.io.Serializable;

public class FriendMapping implements Serializable {
	
	public FriendMapping(){}
	
	int friend_id;
	String friend_fname;
	String friend_lname;
	String friend_email;
	String friend_type;
	
	public String getFriend_type() {
		return friend_type;
	}
	public void setFriend_type(String friend_type) {
		this.friend_type = friend_type;
	}
	public int getFriend_id() {
		return friend_id;
	}
	public void setFriend_id(int friend_id) {
		this.friend_id = friend_id;
	}
	public String getFriend_fname() {
		return friend_fname;
	}
	public void setFriend_fname(String friend_fname) {
		this.friend_fname = friend_fname;
	}
	public String getFriend_lname() {
		return friend_lname;
	}
	public void setFriend_lname(String friend_lname) {
		this.friend_lname = friend_lname;
	}
	public String getFriend_email() {
		return friend_email;
	}
	public void setFriend_email(String friend_email) {
		this.friend_email = friend_email;
	}


	

}
